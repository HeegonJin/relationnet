def copy_to_experiment_dir(model_checkpoints_folder, model_name):
    now_time = datetime.now().strftime('%Y-%m-%d_%H:%M:%S')
    new_exp_dir = os.path.join('./weights/experiments', model_name + '_checkpoints', now_time)
    if not os.path.exists(new_exp_dir):
        os.makedirs(new_exp_dir)
    for src in os.listdir(model_checkpoints_folder):
        shutil.move(os.path.join(model_checkpoints_folder, src), new_exp_dir)
        
def get_mean_and_std(dataset):
    '''Compute the mean and std value of dataset.'''
    dataloader = torch.utils.data.DataLoader(dataset, batch_size=1, shuffle=True, num_workers=2)
    mean = torch.zeros(3)
    std = torch.zeros(3)
    print('==> Computing mean and std..')
    for inputs, targets in dataloader:
        for i in range(3):
            mean[i] += inputs[:,i,:,:].mean()
            std[i] += inputs[:,i,:,:].std()
    mean.div_(len(dataset))
    std.div_(len(dataset))
    return mean, std

def init_params(net):
    '''Init layer parameters.'''
    for m in net.modules():
        if isinstance(m, nn.Conv2d):
            init.kaiming_normal(m.weight, mode='fan_out')
            if m.bias:
                init.constant(m.bias, 0)
        elif isinstance(m, nn.BatchNorm2d):
            init.constant(m.weight, 1)
            init.constant(m.bias, 0)
        elif isinstance(m, nn.Linear):
            init.normal(m.weight, std=1e-3)
            if m.bias:
                init.constant(m.bias, 0)

